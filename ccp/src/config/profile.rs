use serde::{Deserialize, Serialize};
use std::collections::HashMap;

/// 一个 Claude Code 提供商配置
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct Profile {
    /// 配置名称，也是 `ccp-<name>` 命令名，仅允许小写字母、数字、连字符
    pub name: String,
    /// 是否启用
    #[serde(default = "default_enabled")]
    pub enabled: bool,
    /// 可选分组标签
    #[serde(default)]
    pub group: Option<String>,
    /// 环境变量键值对（全部透传给 claude）
    pub vars: HashMap<String, String>,
}

fn default_enabled() -> bool {
    true
}

impl Profile {
    pub fn new(name: String) -> Self {
        Self {
            name,
            enabled: true,
            group: None,
            vars: HashMap::new(),
        }
    }

    /// 校验 name 合法性
    pub fn validate(&self) -> Result<(), String> {
        if self.name.is_empty() {
            return Err("配置名称不能为空".to_string());
        }
        let valid = self
            .name
            .chars()
            .all(|c| c.is_ascii_lowercase() || c.is_ascii_digit() || c == '-');
        if !valid {
            return Err(format!(
                "配置名称「{}」只能包含小写字母、数字和连字符",
                self.name
            ));
        }
        Ok(())
    }

    /// 生成包装脚本的命令行（export + exec claude）
    pub fn to_script(&self) -> String {
        let mut lines = vec![
            "#!/bin/bash".to_string(),
            "# Generated by ccp — edit in TUI, changes here will be overwritten".to_string(),
        ];

        // 按 key 排序保证输出稳定
        let mut keys: Vec<&String> = self.vars.keys().collect();
        keys.sort();
        for key in keys {
            let val = &self.vars[key];
            // 使用单引号包裹，值中的单引号替换为 '\''（shell 安全序列）
            let escaped = val.replace('\'', "'\\''");
            lines.push(format!("export {}='{}'", key, escaped));
        }

        lines.push("exec claude \"$@\"".to_string());
        lines.push("".to_string());
        lines.join("\n")
    }
}